    def update_time(self, n):

        self.update_time_labels()

        time_thread = threading.Timer(1.0, lambda: self.update_time(n + 1))
        time_thread.daemon = True
        time_thread.start()

        if n >= 3600:
            update_weather_thread = threading.Thread(target=self.update_weather())
            update_weather_thread.daemon = True
            update_weather_thread.start()
            n = 0
            print("updating weather...")
        if n % 8 == 0:
            news_feed_thread = threading.Thread(target=self.update_news_feed())
            news_feed_thread.daemon = True
            news_feed_thread.start()

        if n % 2 == 0:
            spotify_thread = threading.Thread(target=self.update_spotify())
            spotify_thread.daemon = True
            spotify_thread.start()

        time = datetime.datetime.now()
        if self.date_label != datetime.datetime.today().date():
            update_date_thread = threading.Thread(target=self.update_date())
            update_date_thread.daemon = True
            update_date_thread.start()

