    def update_spotify(self):
        data = self.spotify.get_data()
        if data:
            self.spotify_titel_label.setText(data["name"])
            self.spotify_album_label.setText(data["album_name"])
            self.spotify_device_label.setText(data["device"])
            self.spotify_artists_label.setText(", ".join(data["artist"]))
