            progress = data["progress"]
            self.spotify_progress_duration_label.setText(
                f"{progress['minutes']:02}:{progress['seconds']:02} / {duration['minutes']:02}:{duration['seconds']:02}")

            if data["is_playing"]:
                self.play_stop_icon.setPixmap(QtGui.QPixmap("img/play_icon.png"))
            else:
                self.play_stop_icon.setPixmap(QtGui.QPixmap("img/pause_icon.png"))

